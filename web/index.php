<h3>Overview</h3>

Chickenfoot is a Firefox extension that puts a programming environment in the browser's sidebar
so you can write scripts to manipulate web pages and automate web browsing.
In Chickenfoot, scripts are written in a superset of Javascript
that includes special functions specific to web tasks.

